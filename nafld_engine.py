from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional, Callable
from datetime import datetime, timezone


# =========================================================
# OUTPUT TYPES
# =========================================================

@dataclass
class Action:
    code: str
    label: str
    urgency: Optional[str] = None
    details: Dict[str, Any] = field(default_factory=dict)
    override_options: Optional[Dict[str, Any]] = None
    display: Dict[str, Any] = field(default_factory=dict)


@dataclass
class DataRequest:
    blocking_node: str
    missing_fields: List[str]
    message: str
    urgency_context: Optional[str] = None
    suggested_actions: List[Action] = field(default_factory=list)


@dataclass
class Stop:
    reason: str
    actions: List[Action] = field(default_factory=list)
    urgency: Optional[str] = None


Output = Action | DataRequest | Stop


# =========================================================
# DECISION LOG
# =========================================================

@dataclass
class DecisionLog:
    node: str
    decision: str
    used_inputs: Dict[str, Any]
    outputs: List[Dict[str, Any]]
    timestamp: str = field(default_factory=lambda: datetime.now(timezone.utc).isoformat())


@dataclass
class Override:
    target_node: str
    field: str
    old_value: Any
    new_value: Any
    reason: str
    created_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))


# =========================================================
# CONTEXT
# =========================================================

@dataclass
class Context:
    data: Dict[str, Any]
    logs: List[DecisionLog] = field(default_factory=list)
    overrides: List[Override] = field(default_factory=list)

    def get(self, key, default=None):
        return self.data.get(key, default)

    def set(self, key, value):
        self.data[key] = value

    def apply_override(self, node, field, value):
        for o in reversed(self.overrides):
            if o.target_node == node and o.field == field:
                return o.new_value
        return value

    def log(self, node, decision, inputs, outputs):
        self.logs.append(
            DecisionLog(
                node=node,
                decision=decision,
                used_inputs=inputs,
                outputs=[{"type": type(o).__name__} for o in outputs],
            )
        )


# =========================================================
# ENGINE CORE
# =========================================================

@dataclass
class NodeResult:
    outputs: List[Output]
    next_node: Optional[str] = None


NodeFn = Callable[[Context], NodeResult]


@dataclass
class Node:
    node_id: str
    fn: NodeFn


class PathwayEngine:
    def __init__(self, nodes: Dict[str, Node], start_node: str):
        self.nodes = nodes
        self.start_node = start_node

    def run(self, ctx: Context) -> List[Output]:
        outputs: List[Output] = []
        current = self.start_node
        guard = 0

        while current:
            guard += 1
            if guard > 300:
                outputs.append(Stop(reason="Safety stop: possible infinite loop"))
                break

            if current not in self.nodes:
                outputs.append(Stop(reason=f"Unknown node: {current}"))
                break

            result = self.nodes[current].fn(ctx)
            outputs.extend(result.outputs)

            if any(isinstance(o, (Stop, DataRequest)) for o in result.outputs):
                break

            current = result.next_node

        return outputs


# =========================================================
# HELPERS
# =========================================================

def _entry_met(ctx: Context) -> bool:
    return bool(ctx.get("incidental_abnormal_alt")) or bool(ctx.get("incidental_ultrasound_fatty_liver"))


def _compute_significant_alcohol(ctx: Context) -> Optional[bool]:
    sex = ctx.get("sex")
    drinks = ctx.get("average_drinks_per_day")

    if sex is None or drinks is None:
        return None

    if sex == "male":
        return drinks > 2
    if sex == "female":
        return drinks > 1
    return None


def _compute_fib4(ctx: Context) -> Dict[str, Any]:
    age = ctx.get("age")
    ast = ctx.get("ast")
    alt = ctx.get("alt")
    platelets = ctx.get("platelets")

    missing = []
    if age is None:
        missing.append("age")
    if ast is None:
        missing.append("ast")
    if alt is None:
        missing.append("alt")
    if platelets is None:
        missing.append("platelets")

    if missing:
        return {"blocked": True, "missing": missing, "fib4": None}

    if alt <= 0 or platelets <= 0:
        return {"blocked": True, "missing": [], "invalid_values": True, "fib4": None}

    fib4 = (age * ast) / (platelets * (alt ** 0.5))
    return {"blocked": False, "missing": [], "fib4": round(fib4, 3)}


