from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional, Callable
from datetime import datetime, timezone


# =========================
# Output types (UI friendly)
# =========================

@dataclass
class Action:
    code: str
    label: str
    urgency: Optional[str] = None   # "urgent" | "semi_urgent" | "routine" | None
    details: Dict[str, Any] = field(default_factory=dict)
    override_options: Optional[Dict[str, Any]] = None

    # NEW: UI/report display metadata
    display: Dict[str, Any] = field(default_factory=dict)

@dataclass
class DataRequest:
    blocking_node: str
    missing_fields: List[str]
    message: str
    urgency_context: Optional[str] = None
    suggested_actions: List[Action] = field(default_factory=list)

@dataclass
class Stop:
    reason: str
    actions: List[Action] = field(default_factory=list)
    urgency: Optional[str] = None

Output = Action | DataRequest | Stop


# =========================
# Audit trail + overrides
# =========================

@dataclass
class DecisionLog:
    node: str
    decision: str
    used_inputs: Dict[str, Any]
    outputs: List[Dict[str, Any]] = field(default_factory=list)
    timestamp: str = field(default_factory=lambda: datetime.now(timezone.utc).isoformat())

@dataclass
class Override:
    target_node: str
    field: str
    old_value: Any
    new_value: Any
    reason: str

    # Store as datetime for stable ordering; serialize when needed
    created_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))

# =========================
# Context
# =========================

@dataclass
class Context:
    data: Dict[str, Any]
    logs: List[DecisionLog] = field(default_factory=list)
    overrides: List[Override] = field(default_factory=list)

    def get(self, key: str, default=None):
        return self.data.get(key, default)

    def set(self, key: str, value: Any):
        self.data[key] = value

    def apply_override(self, node: str, field: str, current_value: Any) -> Any:
        candidates = [o for o in self.overrides if o.target_node == node and o.field == field]
        if not candidates:
            return current_value
        newest = max(candidates, key=lambda o: o.created_at)
        return newest.new_value
    def add_override(self, target_node: str, field: str, new_value: Any, reason: str):
        """
        Convenience helper for UI/testing.
        Looks up the current value in Context.data if present (optional),
        stores an Override object, and allows apply_override() to pick it up.
        """
        old_value = self.data.get(field, None)
        self.overrides.append(
            Override(
                target_node=target_node,
                field=field,
                old_value=old_value,
                new_value=new_value,
                reason=reason,
            )
        )

    def log(self, node: str, decision: str, used_inputs: Dict[str, Any], outputs: List[Output]):
        def ser_action(a: Action) -> Dict[str, Any]:
            return {
                "type": "action",
                "code": a.code,
                "label": a.label,
                "urgency": a.urgency,
                "details": a.details,
                "override_options": a.override_options,
                "display": a.display,
            }

        def ser(o: Output) -> Dict[str, Any]:
            if isinstance(o, Action):
                return ser_action(o)

            if isinstance(o, DataRequest):
                return {
                    "type": "data_request",
                    "blocking_node": o.blocking_node,
                    "missing_fields": o.missing_fields,
                    "message": o.message,
                    "urgency_context": o.urgency_context,
                    "suggested_actions": [ser_action(a) for a in o.suggested_actions],
                }

            if isinstance(o, Stop):
                return {
                    "type": "stop",
                    "reason": o.reason,
                    "urgency": o.urgency,
                    "actions": [ser_action(a) for a in o.actions],
                }

            return {"type": "unknown"}

        self.logs.append(
            DecisionLog(
                node=node,
                decision=decision,
                used_inputs=used_inputs,
                outputs=[ser(x) for x in outputs],
            )
        )

# =========================
# Node engine
# =========================

@dataclass
class NodeResult:
    outputs: List[Output]
    next_node: Optional[str] = None

NodeFn = Callable[[Context], NodeResult]

@dataclass
class Node:
    node_id: str
    fn: NodeFn

class PathwayEngine:
    def __init__(self, nodes: Dict[str, Node], start_node: str):
        self.nodes = nodes
        self.start_node = start_node

    def run(self, ctx: Context) -> List[Output]:
        outputs: List[Output] = []
        current = self.start_node
        guard = 0

        while current is not None:
            guard += 1
            if guard > 250:
                outputs.append(Stop(reason="Safety stop: possible infinite loop"))
                break
            if current not in self.nodes:
                outputs.append(Stop(reason=f"Engine error: unknown node '{current}' (typo or missing registration)."))
                break
            result = self.nodes[current].fn(ctx)
            outputs.extend(result.outputs)

            if any(isinstance(o, (DataRequest, Stop)) for o in result.outputs):
                break

            current = result.next_node

        return outputs


# ==========================================================
# IDA PATHWAY IMPLEMENTATION
# ==========================================================

def ida_build_engine() -> PathwayEngine:
    nodes: Dict[str, Node] = {}

    # -------------------------
    # Helpers
    # -------------------------

    def _normalize_scope_flag(ctx: Context) -> Optional[bool]:
        """
        Accepts either:
        - complete_colonoscopy_within_2y
        - lower_endoscopy_within_2y (alias)
        Preference: complete_colonoscopy_within_2y if present.
        """
        v = ctx.get("complete_colonoscopy_within_2y")
        if v is not None:
            return v
        return ctx.get("lower_endoscopy_within_2y")

    def _iron_deficiency(ctx: Context) -> Dict[str, Any]:
        """
        Returns dict:
          {
            "blocked": bool,
            "missing": [...],
            "iron_deficient": bool|None,
            "ferritin_cutoff": float|None,
            "notes": [...]
          }
        """
        notes: List[str] = []
        sex = ctx.get("sex")
        ferritin = ctx.get("ferritin")
        inflammation = ctx.get("inflammation_present")
        tsat = ctx.get("transferrin_saturation")
        ckd = ctx.get("chronic_kidney_disease")  # optional caveat variable

        missing = []
        if sex is None:
            missing.append("sex")
        if ferritin is None:
            missing.append("ferritin")

        if missing:
            return {"blocked": True, "missing": missing, "iron_deficient": None, "ferritin_cutoff": None, "notes": notes}

        ferritin_cutoff = 30.0 if sex == "male" else 20.0
        notes.append(f"Base ferritin cutoff used: {ferritin_cutoff} (sex-specific)")

        if inflammation is True:
            ferritin_cutoff = 100.0
            notes.append("Inflammation present: ferritin <100 supports iron deficiency (acute phase reactant adjustment)")

        # Pathway text caveat: ferritin >100 and no significant CKD -> iron deficiency very unlikely
        if ferritin > 100.0 and not ckd:
            iron_deficient = False
            notes.append("Ferritin >100 and no significant CKD: iron deficiency very unlikely per pathway text")
        else:
            iron_deficient = ferritin < ferritin_cutoff

        # TSAT <16 supports diagnosis
        if tsat is not None and tsat < 16.0:
            iron_deficient = True
            notes.append("Transferrin saturation <16% supports iron deficiency -> set True")

        return {"blocked": False, "missing": [], "iron_deficient": iron_deficient, "ferritin_cutoff": ferritin_cutoff, "notes": notes}

    def _urgent_alarm_subset(ctx: Context) -> bool:
        return any([
            bool(ctx.get("significant_diarrhea")),
            bool(ctx.get("weight_loss")),
            bool(ctx.get("progressive_bowel_change")),
            bool(ctx.get("significant_abdominal_pain")),
        ])

    def _triage_modifiers_present(ctx: Context) -> bool:
        return any([
            bool(ctx.get("nocturnal_symptoms")),
            bool(ctx.get("family_history_crc_first_degree")),
            bool(ctx.get("family_history_ibd_first_degree")),
            bool(ctx.get("evidence_of_gi_bleeding")),
            # IDA onset age >40 is a modifier; if age available we can compute later
        ])

    def _freshness_flag(days_ago: Optional[int], within_flag: Optional[bool]) -> Optional[bool]:
        """
        If within_flag provided, use it.
        Else if days_ago provided, compute <=56 days (~8 weeks).
        Else None.
        """
        if within_flag is not None:
            return within_flag
        if days_ago is None:
            return None
        return days_ago <= 56

    # -------------------------
    # Nodes
    # -------------------------

    def node_confirm_ida(ctx: Context) -> NodeResult:
        node_id = "Confirm_IDA"

        hb = ctx.get("hemoglobin")
        sex = ctx.get("sex")

        missing = []
        if hb is None:
            missing.append("hemoglobin")
        if ctx.get("ferritin") is None:
            missing.append("ferritin")
        if sex is None:
            missing.append("sex")

        if missing:
            dr = DataRequest(
                blocking_node=node_id,
                missing_fields=missing,
                message="Cannot confirm IDA without hemoglobin, ferritin, and sex (thresholds are sex-specific).",
                suggested_actions=[
                    Action(code="ORDER_HB", label="Obtain hemoglobin (Hb)"),
                    Action(code="ORDER_FERRITIN", label="Obtain serum ferritin"),
                    Action(code="CAPTURE_SEX", label="Record patient sex"),
                ],
            )
            ctx.log(node_id, "BLOCKED_MISSING_DATA", {"missing": missing}, [dr])
            return NodeResult(outputs=[dr])

        # Anemia definition (pathway text)
        anemia = False
        if sex == "male" and hb < 130:
            anemia = True
        if sex == "female" and hb < 120:
            anemia = True

        iron = _iron_deficiency(ctx)
        if iron["blocked"]:
            dr = DataRequest(
                blocking_node=node_id,
                missing_fields=iron["missing"],
                message="Cannot determine iron deficiency without sex and ferritin.",
            )
            ctx.log(node_id, "BLOCKED_MISSING_DATA", {"missing": iron["missing"]}, [dr])
            return NodeResult(outputs=[dr])

        ida_confirmed = bool(anemia and iron["iron_deficient"])
        ida_confirmed = ctx.apply_override(node_id, "ida_confirmed", ida_confirmed)
        ctx.set("ida_confirmed", ida_confirmed)

        if not ida_confirmed:
            stop = Stop(
                reason=(
                    "IDA not confirmed: both anemia and iron deficiency must be present. "
                    "Consider alternative anemia etiologies and interpret iron indices accordingly."
                ),
                actions=[Action(code="ALT_ANEMIA_WORKUP", label="Investigate non-IDA anemia causes / alternative etiologies")],
            )
            ctx.log(
                node_id,
                "IDA_NOT_CONFIRMED",
                {"hemoglobin": hb, "sex": sex, "ferritin": ctx.get("ferritin")},
                [stop],
            )
            return NodeResult(outputs=[stop])

        out = [
            Action(
                code="IDA_CONFIRMED",
                label="IDA confirmed (low Hb + low iron stores).",
                details={
                    "supported_by": [
                        f"Hb {hb} g/L is below the anemia threshold for sex ({'130' if sex == 'male' else '120'} g/L)",
                        f"Ferritin interpretation supports iron deficiency using cutoff {iron['ferritin_cutoff']}",
                    ] + (["Transferrin saturation <16% supports iron deficiency"] if ctx.get("transferrin_saturation") is not None and ctx.get("transferrin_saturation") < 16 else []),
                    "anemia": anemia,
                    "iron_deficient": iron["iron_deficient"],
                    "ferritin_cutoff_used": iron["ferritin_cutoff"],
                    "notes": iron["notes"],
                },
                override_options={
                    "node": node_id,
                    "field": "ida_confirmed",
                    "allowed": [True, False],
                    "reason_required": True,
                },
                display={
                    "role": "support",
                    "badge": "info",
                    "show_as_standalone": True,
                    "sort_priority": 20,
                },
            )
        ]
        ctx.log(node_id, "IDA_CONFIRMED", {"hemoglobin": hb, "sex": sex, "ferritin": ctx.get("ferritin")}, out)
        return NodeResult(outputs=out, next_node="Urgency_Triage")

    def node_urgency(ctx: Context) -> NodeResult:
        node_id = "Urgency_Triage"

        hb = ctx.get("hemoglobin")
        sex = ctx.get("sex")

        # Safety progressive gating (should already exist from IDA confirm)
        missing = []
        if hb is None:
            missing.append("hemoglobin")
        if sex is None:
            missing.append("sex")
        if missing:
            dr = DataRequest(
                blocking_node=node_id,
                missing_fields=missing,
                message="Cannot assign urgency without hemoglobin and sex.",
                suggested_actions=[Action(code="ORDER_HB", label="Obtain hemoglobin (Hb)"), Action(code="CAPTURE_SEX", label="Record patient sex")]
            )
            ctx.log(node_id, "BLOCKED_MISSING_DATA", {"missing": missing}, [dr])
            return NodeResult(outputs=[dr])

        # Compute “unexplained” alarm symptom status:
        # Pathway text qualifies alarm symptoms as “unexplained (i.e., not investigated by lower endoscopy in the last 2 years)”
        urgent_alarm_present = _urgent_alarm_subset(ctx)

        scope_within_2y = _normalize_scope_flag(ctx)

        investigated = ctx.get("alarm_features_investigated_by_lower_endoscopy_within_2y")

        alarm_unexplained: Optional[bool] = None
        if urgent_alarm_present:
            if investigated is None:
                # progressive gate ONLY on whether the alarm features were investigated
                dr = DataRequest(
                    blocking_node=node_id,
                    missing_fields=["alarm_features_investigated_by_lower_endoscopy_within_2y"],
                    message=(
                        "Urgent alarm symptom(s) present. Need to know whether these alarm features have been investigated "
                        "by lower endoscopy within the last 2 years (not just whether a colonoscopy occurred)."
                    ),
                    suggested_actions=[
                        Action(
                            code="CAPTURE_ALARM_INVESTIGATION",
                            label="Document whether current alarm features were investigated by lower endoscopy within last 2 years"
                        )
                    ]
                )
                ctx.log(node_id, "BLOCKED_MISSING_DATA", {"urgent_alarm_present": True, "scope_within_2y_context_only": scope_within_2y}, [dr])
                return NodeResult(outputs=[dr])

            alarm_unexplained = (investigated is False)

        # Hb urgent criteria per text
        urgent_hb = (hb < 110) if sex == "male" else (hb < 100)

        # Hb semi-urgent band
        semi_urgent = False
        if sex == "male":
            semi_urgent = 110 <= hb < 130
        else:
            semi_urgent = 100 <= hb < 120

        # Baseline drop nuance:
        # Text notes that some patients (CKD/elderly/anemia of chronic disease) may have baseline Hb below normal.
        # A significant unexplained drop from baseline is a reason for referral.
        baseline_hb = ctx.get("baseline_hemoglobin")
        hb_drop = ctx.get("hemoglobin_drop_g_per_l")

        if hb_drop is None and baseline_hb is not None and hb is not None:
            hb_drop = baseline_hb - hb
            ctx.set("hemoglobin_drop_g_per_l", hb_drop)

        # We do NOT block if baseline is missing; we only incorporate if available.
        urgent_due_to_hb_drop = False
        if hb_drop is not None:
            # No numeric cutoff provided in text; treat as a clinician-judgement hook.
            # Default heuristic: any drop >= 20 g/L is noteworthy; can be overridden.
            drop_unexplained = ctx.get("drop_unexplained")
            if drop_unexplained is None:
                # If not specified, we treat as unknown and do not force urgent on drop alone.
                urgent_due_to_hb_drop = False
            else:
                urgent_due_to_hb_drop = (hb_drop >= 20.0 and drop_unexplained is True)

            urgent_due_to_hb_drop = ctx.apply_override(node_id, "urgent_due_to_hb_drop", urgent_due_to_hb_drop)

        # Urgency decision:
        # Urgent if urgent_hb OR (urgent alarms present AND unexplained)
        urgent_alarm_qualifies = bool(urgent_alarm_present and (alarm_unexplained is True))
        urgent_alarm_qualifies = ctx.apply_override(node_id, "urgent_alarm_qualifies", urgent_alarm_qualifies)

        if urgent_hb or urgent_alarm_qualifies or urgent_due_to_hb_drop:
            urgency = "urgent"
            label = "Urgent: gastroscopy + colonoscopy (<2 weeks)"
        elif semi_urgent:
            urgency = "semi_urgent"
            label = "Semi-urgent: endoscopy referral"
        else:
            urgency = "routine"
            label = "Routine: endoscopy referral"

        urgency = ctx.apply_override(node_id, "urgency", urgency)
        ctx.set("urgency", urgency)

        supported_by = []
        if urgent_hb:
            supported_by.append("Hb below urgent threshold for sex")
        if urgent_alarm_qualifies:
            supported_by.append("Unexplained urgent alarm symptom(s) present")
        if urgent_due_to_hb_drop:
            supported_by.append("Significant unexplained Hb drop from baseline")
        if semi_urgent and not (urgent_hb or urgent_alarm_qualifies or urgent_due_to_hb_drop):
            supported_by.append("Hb in semi-urgent band for sex")
        if (not urgent_hb) and (not urgent_alarm_qualifies) and (not urgent_due_to_hb_drop) and (not semi_urgent):
            supported_by.append("Hb not in urgent/semi-urgent thresholds and no qualifying alarms")

        out = [Action(
            code="ASSIGN_URGENCY",
            label=label,
            urgency=urgency,
            details={
                "supported_by": supported_by,
                "urgent_hb_threshold_met": urgent_hb,
                "urgent_alarm_present": urgent_alarm_present,
                "scope_within_2y_context_only": scope_within_2y,
                "alarm_features_investigated_by_lower_endoscopy_within_2y": investigated,
                "alarm_unexplained": alarm_unexplained,
                "urgent_alarm_qualifies": urgent_alarm_qualifies,
                "baseline_hemoglobin": baseline_hb,
                "hemoglobin_drop_g_per_l": hb_drop,
                "urgent_due_to_hb_drop": urgent_due_to_hb_drop,
                "semi_urgent_band_met": semi_urgent,
            },
            display={
                "role": "primary_decision",
                "show_as_standalone": True,
                "sort_priority": 10,
            },
        )]
        ctx.log(
            node_id,
            "URGENCY_ASSIGNED",
            {
                "hemoglobin": hb,
                "sex": sex,
                "urgent_alarm_present": urgent_alarm_present,
                "alarm_features_investigated_by_lower_endoscopy_within_2y": investigated,
                "alarm_unexplained": alarm_unexplained,
                "urgent_alarm_qualifies": urgent_alarm_qualifies,
                "baseline_hemoglobin": baseline_hb,
                "hemoglobin_drop_g_per_l": hb_drop,
                "drop_unexplained": ctx.get("drop_unexplained"),
                "urgent_due_to_hb_drop": urgent_due_to_hb_drop,
                "scope_within_2y_context_only": scope_within_2y,
            },
            out,
        )
        return NodeResult(outputs=out, next_node="Celiac_RuleOut")

    def node_celiac(ctx: Context) -> NodeResult:
        node_id = "Celiac_RuleOut"
        urgency = ctx.get("urgency")

        # Collect non-blocking reminders/recommendations so they actually appear in outputs
        extra_outputs: List[Output] = []

        # Exemption: prior normal duodenal biopsy
        if ctx.get("prior_duodenal_biopsy_normal") is True:
            out = [
                Action(
                    code="CELIAC_EXEMPT",
                    label="Celiac rule-out not required: prior normal duodenal biopsy documented.",
                    urgency=urgency,
                    details={
                        "supported_by": ["Prior normal duodenal biopsy documented"],
                    },
                    display={
                        "role": "support",
                        "badge": "info",
                        "show_as_standalone": True,
                        "sort_priority": 30,
                    },
                )
            ]
            ctx.log(node_id, "EXEMPT_BIOPSY_NORMAL", {"prior_duodenal_biopsy_normal": True}, out)
            return NodeResult(outputs=out, next_node="Gynecology_Check")

        # Need TTG recency to apply the <5y rule (TTG not required if done within last 5 years)
        years_since = ctx.get("ttg_years_since_test")
        if years_since is None:
            dr = DataRequest(
                blocking_node=node_id,
                missing_fields=["ttg_years_since_test"],
                message="Need years since last TTG to apply the <5-year rule (TTG not required if done within last 5 years).",
                urgency_context=urgency,
                suggested_actions=[
                    Action(
                        code="CAPTURE_TTG_RECENCY",
                        label="Enter years since last TTG (<5 is recent)",
                        urgency=urgency
                    )
                ],
            )
            ctx.log(node_id, "BLOCKED_MISSING_DATA", {"missing": ["ttg_years_since_test"]}, [dr])
            return NodeResult(outputs=[dr])

        # If TTG is outdated (>=5 years), we cannot make a decision from the old result.
        # Require a repeat TTG result specifically (ttg_positive_repeat).
        if years_since >= 5:
            order = Action(
                code="ORDER_TTG",
                label="TTG is outdated (≥5 years): order/repeat TTG to rule out celiac (IgA not required per pathway text).",
                urgency=urgency,
                details={"ttg_years_since_test": years_since},
            )
            extra_outputs.append(order)

