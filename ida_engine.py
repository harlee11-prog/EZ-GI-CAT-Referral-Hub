    def node_finalize(ctx: Context) -> NodeResult:
        node_id = "Finalize"
        urgency = ctx.get("urgency")

        reminders: List[Action] = []

        # Medication review reminders (non-blocking)
        if ctx.get("anticoagulants") is None:
            reminders.append(Action(code="CAPTURE_ANTICOAG", label="Document anticoagulant use (triage information).", urgency=urgency))
        if ctx.get("antiplatelets") is None:
            reminders.append(Action(code="CAPTURE_ANTIPLATELET", label="Document antiplatelet use (triage information).", urgency=urgency))
        if ctx.get("nsaid_use") is None:
            reminders.append(Action(code="CAPTURE_NSAID", label="Document NSAID use (possible contributor to chronic GI blood loss).", urgency=urgency))

        # Physical exam reminder
        if (ctx.get("progressive_bowel_change") or ctx.get("significant_abdominal_pain")) and ctx.get("rectal_exam_done") is None:
            reminders.append(Action(code="CONSIDER_RECTAL_EXAM", label="Consider/document rectal exam (esp. with bowel habit change or lower abdominal pain).", urgency=urgency))

        # --- NEW: referral decision can be overridden ---
        # Default recommendation is True (refer).
        refer_default = True
        refer_final = ctx.apply_override(node_id, "refer_endoscopy", refer_default)

        # Always output the referral Action, but indicate the "final" decision in details.
        referral_action = Action(
            code="REFER_ENDOSCOPY",
            label="Refer for gastroscopy + colonoscopy per urgency.",
            urgency=urgency,
            details={
                "supported_by": [
                    f"Final triage urgency is {urgency}",
                    "IDA pathway reached final referral step",
                ],
                "recommended_by_system": refer_default,
                "final_decision": refer_final,
            },
            override_options={
                "node": node_id,
                "field": "refer_endoscopy",
                "allowed": [True, False],
                "reason_required": True,
            },
            display={
                "role": "primary_decision",
                "group_under": "ASSIGN_URGENCY",
                "show_as_standalone": False,
                "sort_priority": 70,
            },
        )

        # If clinician overrides to False, stop with a clear reason.
        if refer_final is False:
            stop = Stop(
                reason="Endoscopy referral was overridden by clinician (system recommendation declined).",
                actions=[referral_action] + reminders,
                urgency=urgency,
            )
            ctx.log(
                node_id,
                "STOP_REFERRAL_OVERRIDDEN",
                {"urgency": urgency, "refer_default": refer_default, "refer_final": refer_final},
                [stop],
            )
            return NodeResult(outputs=[stop])

        # Otherwise, normal stop
        stop = Stop(
            reason="IDA pathway complete: proceed with endoscopic evaluation per triage urgency and documented context.",
            actions=[referral_action] + reminders,
            urgency=urgency,
        )
        ctx.log(
            node_id,
            "STOP",
            {"urgency": urgency, "refer_default": refer_default, "refer_final": refer_final},
            [stop],
        )
        return NodeResult(outputs=[stop])
    # =========================
    # Register nodes
    # =========================

    nodes["Confirm_IDA"] = Node("Confirm_IDA", node_confirm_ida)
    nodes["Urgency_Triage"] = Node("Urgency_Triage", node_urgency)
    nodes["Celiac_RuleOut"] = Node("Celiac_RuleOut", node_celiac)
    nodes["Gynecology_Check"] = Node("Gynecology_Check", node_gyne)
    nodes["HighRiskRectalBleeding_Check"] = Node("HighRiskRectalBleeding_Check", node_hrrb)
    nodes["FIT_Warning"] = Node("FIT_Warning", node_fit_warning)
    nodes["Baseline_Investigation_Freshness"] = Node("Baseline_Investigation_Freshness", node_lab_freshness)
    nodes["Triage_Modifiers"] = Node("Triage_Modifiers", node_triage_modifiers)
    nodes["Finalize"] = Node("Finalize", node_finalize)

    # =========================
    # Build and return engine
    # =========================

    return PathwayEngine(
        nodes=nodes,
        start_node="Confirm_IDA",
    )

