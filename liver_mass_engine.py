VALID_LESION_TYPES = {
    "simple_cyst",
    "complex_cyst",
    "hemangioma",
    "fnh",
    "adenoma",
    "indeterminate_suspicious",
    "solid_unspecified",
    "metastatic_disease",
}

SOLID_TYPES = {
    "hemangioma",
    "fnh",
    "adenoma",
    "indeterminate_suspicious",
    "solid_unspecified",
    "metastatic_disease",
}

CYST_TYPES = {"simple_cyst", "complex_cyst"}


def _compute_is_cyst(ctx: Context) -> bool:
    lesion_type = ctx.get("lesion_type")
    return lesion_type in CYST_TYPES


def _compute_is_solid(ctx: Context) -> bool:
    lesion_type = ctx.get("lesion_type")
    return lesion_type in SOLID_TYPES


def _compute_high_risk(ctx: Context) -> Dict[str, Any]:
    chronic_viral_hepatitis = bool(ctx.get("chronic_hepatitis_b")) or bool(ctx.get("chronic_hepatitis_c"))
    result = {
        "history_cirrhosis": bool(ctx.get("history_cirrhosis")),
        "chronic_hepatitis_b": bool(ctx.get("chronic_hepatitis_b")),
        "chronic_hepatitis_c": bool(ctx.get("chronic_hepatitis_c")),
        "chronic_viral_hepatitis": chronic_viral_hepatitis,
        "prior_malignancy_any_location": bool(ctx.get("prior_malignancy_any_location")),
        "clinician_concern_malignancy": bool(ctx.get("clinician_concern_malignancy")),
    }
    result["high_risk"] = any(
        [
            result["history_cirrhosis"],
            result["chronic_viral_hepatitis"],
            result["prior_malignancy_any_location"],
            result["clinician_concern_malignancy"],
        ]
    )
    return result


def _compute_hemorrhage_red_flag(ctx: Context) -> bool:
    return bool(
        _compute_is_solid(ctx)
        and ctx.get("episodic_epigastric_or_ruq_pain")
        and ctx.get("hypotension")
    )


