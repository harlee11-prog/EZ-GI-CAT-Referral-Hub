def run_chronic_diarrhea_pathway(patient_data: Dict[str, Any], overrides=None):

    engine = chronic_diarrhea_build_engine()

    ctx = Context(
        data=dict(patient_data),
        overrides=overrides or []
    )

    outputs = engine.run(ctx)

    return outputs, ctx.logs, ctx.overrides