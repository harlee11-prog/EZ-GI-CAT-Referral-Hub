from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional, Callable
from datetime import datetime, timezone


# =========================
# Output types
# =========================

@dataclass
class Action:
    code: str
    label: str
    urgency: Optional[str] = None
    details: Dict[str, Any] = field(default_factory=dict)
    override_options: Optional[Dict[str, Any]] = None
    display: Dict[str, Any] = field(default_factory=dict)

@dataclass
class DataRequest:
    blocking_node: str
    missing_fields: List[str]
    message: str
    urgency_context: Optional[str] = None
    suggested_actions: List[Action] = field(default_factory=list)

@dataclass
class Stop:
    reason: str
    actions: List[Action] = field(default_factory=list)
    urgency: Optional[str] = None

Output = Action | DataRequest | Stop


# =========================
# Audit trail + overrides
# =========================

@dataclass
class DecisionLog:
    node: str
    decision: str
    used_inputs: Dict[str, Any]
    outputs: List[Dict[str, Any]] = field(default_factory=list)
    timestamp: str = field(default_factory=lambda: datetime.now(timezone.utc).isoformat())

@dataclass
class Override:
    target_node: str
    field: str
    old_value: Any
    new_value: Any
    reason: str
    created_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))


# =========================
# Context
# =========================

@dataclass
class Context:
    data: Dict[str, Any]
    logs: List[DecisionLog] = field(default_factory=list)
    overrides: List[Override] = field(default_factory=list)

    def get(self, key: str, default=None):
        return self.data.get(key, default)

    def set(self, key: str, value: Any):
        self.data[key] = value

    def apply_override(self, node: str, field: str, current_value: Any) -> Any:
        candidates = [o for o in self.overrides if o.target_node == node and o.field == field]
        if not candidates:
            return current_value
        newest = max(candidates, key=lambda o: o.created_at)
        return newest.new_value

    def log(self, node: str, decision: str, used_inputs: Dict[str, Any], outputs: List[Output]):
        def ser_action(a: Action) -> Dict[str, Any]:
            return {
                "type": "action",
                "code": a.code,
                "label": a.label,
                "urgency": a.urgency,
                "details": a.details,
                "override_options": a.override_options,
                "display": a.display,
            }

        def ser(o: Output) -> Dict[str, Any]:
            if isinstance(o, Action):
                return ser_action(o)
            if isinstance(o, DataRequest):
                return {
                    "type": "data_request",
                    "blocking_node": o.blocking_node,
                    "missing_fields": o.missing_fields,
                    "message": o.message,
                    "urgency_context": o.urgency_context,
                    "suggested_actions": [ser_action(a) for a in o.suggested_actions],
                }
            if isinstance(o, Stop):
                return {
                    "type": "stop",
                    "reason": o.reason,
                    "urgency": o.urgency,
                    "actions": [ser_action(a) for a in o.actions],
                }
            return {"type": "unknown"}

        self.logs.append(
            DecisionLog(
                node=node,
                decision=decision,
                used_inputs=used_inputs,
                outputs=[ser(x) for x in outputs],
            )
        )


# =========================
# Engine
# =========================

@dataclass
class NodeResult:
    outputs: List[Output]
    next_node: Optional[str] = None

NodeFn = Callable[[Context], NodeResult]

@dataclass
class Node:
    node_id: str
    fn: NodeFn

class PathwayEngine:
    def __init__(self, nodes: Dict[str, Node], start_node: str):
        self.nodes = nodes
        self.start_node = start_node

    def run(self, ctx: Context) -> List[Output]:
        outputs: List[Output] = []
        current = self.start_node
        guard = 0

        while current is not None:
            guard += 1
            if guard > 250:
                outputs.append(Stop(reason="Safety stop: possible infinite loop"))
                break
            if current not in self.nodes:
                outputs.append(Stop(reason=f"Engine error: unknown node '{current}'"))
                break

            result = self.nodes[current].fn(ctx)
            outputs.extend(result.outputs)

            if any(isinstance(o, (DataRequest, Stop)) for o in result.outputs):
                break

            current = result.next_node

        return outputs


