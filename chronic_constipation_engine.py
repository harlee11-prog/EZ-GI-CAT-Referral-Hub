def constipation_build_engine() -> PathwayEngine:
    nodes: Dict[str, Node] = {}

    # -------------------------
    # Helpers
    # -------------------------

    def _derive_constipation_criteria(ctx: Context) -> Dict[str, Any]:
        months = ctx.get("symptom_months_present_last_6")
        sbm = ctx.get("spontaneous_bowel_movements_per_week")
        hard = ctx.get("hard_or_lumpy_stool_percent")
        straining = ctx.get("straining_percent")
        incomplete = ctx.get("incomplete_evacuation_percent")
        blockage = ctx.get("anorectal_blockage_percent")
        manual = ctx.get("manual_maneuvers_percent")

        missing = []
        if months is None:
            missing.append("symptom_months_present_last_6")
        for k, v in {
            "spontaneous_bowel_movements_per_week": sbm,
            "hard_or_lumpy_stool_percent": hard,
            "straining_percent": straining,
            "incomplete_evacuation_percent": incomplete,
            "anorectal_blockage_percent": blockage,
            "manual_maneuvers_percent": manual,
        }.items():
            if v is None:
                missing.append(k)

        if missing:
            return {
                "blocked": True,
                "missing": missing,
                "criteria_met": None,
            }

        c1 = sbm <= 3
        c2 = hard > 25
        c3 = straining > 25
        c4 = incomplete > 25
        c5 = blockage > 25
        c6 = manual > 25

        count = sum([c1, c2, c3, c4, c5, c6])
        criteria_met = bool(months >= 3 and count >= 2)

        return {
            "blocked": False,
            "missing": [],
            "criterion_low_bm_frequency": c1,
            "criterion_hard_stool": c2,
            "criterion_straining": c3,
            "criterion_incomplete_evacuation": c4,
            "criterion_anorectal_blockage": c5,
            "criterion_manual_maneuvers": c6,
            "constipation_symptom_count": count,
            "criteria_met": criteria_met,
            "symptom_months_present_last_6": months,
        }

    def _alarm_features_present(ctx: Context) -> Dict[str, Any]:
        family_crc = bool(ctx.get("family_history_crc_first_degree"))
        weight_loss = ctx.get("weight_loss_percent_6_to_12_months")
        bowel_change = bool(ctx.get("sudden_or_progressive_change_in_bowel_habits"))
        visible_blood = bool(ctx.get("visible_blood_in_stool"))
        suspicious_mass = bool(ctx.get("suspicious_anal_canal_mass_or_irregularity"))
        ida = bool(ctx.get("iron_deficiency_anemia_present"))

        weight_loss_alarm = False
        if weight_loss is not None:
            weight_loss_alarm = weight_loss > 5.0

        present = bool(
            family_crc
            or weight_loss_alarm
            or bowel_change
            or visible_blood
            or suspicious_mass
            or ida
        )

        return {
            "alarm_features_present": present,
            "family_history_crc_first_degree": family_crc,
            "weight_loss_alarm": weight_loss_alarm,
            "sudden_or_progressive_change_in_bowel_habits": bowel_change,
            "visible_blood_in_stool": visible_blood,
            "suspicious_anal_canal_mass_or_irregularity": suspicious_mass,
            "iron_deficiency_anemia_present": ida,
        }

    def _defecatory_dysfunction_features(ctx: Context) -> Dict[str, Any]:
        traumatic = bool(ctx.get("traumatic_perineal_injury_history"))
        blockage = bool(ctx.get("outlet_blockage_sensation"))
        wiggle = bool(ctx.get("needs_to_wiggle_or_rotate_on_toilet"))
        present = bool(traumatic or blockage or wiggle)
        return {
            "defecatory_dysfunction_features_present": present,
            "traumatic_perineal_injury_history": traumatic,
            "outlet_blockage_sensation": blockage,
            "needs_to_wiggle_or_rotate_on_toilet": wiggle,
        }

